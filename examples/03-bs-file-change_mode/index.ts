/** |, &, ^ 都是将数字转换为 2进制 后，根据不同的规则比较相同位置数字的运算 **/

//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


let a,
    b,
    res;


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


/**
 * a | b                        // 或运算
 *
 *
 * 1: true, 0: false            // 比较进制相同位置
 *
 *      01 => 1
 *      00 => 0
 */

// eg                   // 2进制

a = 1;                  // 0001
b = 2;                  // 0010

res = a | b;            // 0011


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


/**
 * a & b                        // 且运算
 *
 *
 * 1: true, 0: false            // 比较进制相同位置
 *
 *      11 => 1
 *      00/10/01 => 0
 */

// eg                   // 2进制

a = 3;                  // 0011
b = 2;                  // 0010

res = a & b;            // 0010


//- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -//


/**
 * a ^ b                        // 异或运算
 *
 *
 * 1: true, 0: false            // 比较进制相同位置
 *
 *      11/00 => 0
 *      10/01 => 1
 */

// eg                   // 2进制

a = 3;                  // 0011
b = 2;                  // 0010

res = a ^ b;            // 0001


//-------------------------------------------------------------------------------------------------------------------//


enum Permission {
    // 1进制
    Read = 1,       // 0001
    Write = 2,      // 0010
    Create = 4,     // 0100
    Delete = 8,     // 1000
}

let p = Permission.Read | Permission.Write;

function hasPermission(target: Permission, per: Permission) {

    return (target & per) === per;
}

p = p ^ Permission.Write;

console.log(hasPermission(p, Permission.Write));


